    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            String UserName = techUserName.getText();
            String Password = techPassword.getText();
            int Level = 0;
            if (rd1.isSelected()) {
                Level = 1;
            } else if (rd2.isSelected()) {
                Level = 2;
            }
            TaiKhoan tk1 = new TaiKhoan(UserName, Password, Level);
            TaiKhoanDAO.insert(tk1);
            showtk();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Tên tài khoản phải trùng với mã nhân viên!");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        try {
            String UserName = String.valueOf(techUserName.getText());
            TaiKhoanDAO tkdao = new TaiKhoanDAO();
            if (tkdao.getTaiKhoanbyUsername(UserName) != null) {
                TaiKhoan tk = tkdao.getTaiKhoanbyUsername(UserName);
                tk.setUsername(techUserName.getText());
                tk.setPassword(techPassword.getText());
                int Level = 0;
                if (rd1.isSelected()) {
                    Level = 1;
                } else if (rd2.isSelected()) {
                    Level = 2;
                }
                tk.setLevel(Level);
                tkdao.update(tk);
                JOptionPane.showMessageDialog(this, "Cập nhật thành công!!");

                showtk();

            } else {
                JOptionPane.showMessageDialog(this, "Tài khoản không tồn tại!", "Error Warning", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tài khoản cần sửa!");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndec = tblTaiKhoan.getSelectedRow();
        if (selectedIndec >= 0) {
            int option = JOptionPane.showConfirmDialog(this, "Bạn có muốn xóa không ? ");
            if (option == 0) {
                String un = String.valueOf(tblTaiKhoan.getValueAt(selectedIndec, 0));
                TaiKhoanDAO.delete(un);
                showtk();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn tài khoản cần xóa!");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

