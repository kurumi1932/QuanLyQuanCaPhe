    private void tblTaiKhoanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTaiKhoanMouseClicked
        try {
            TaiKhoanDAO tkdao = new TaiKhoanDAO();
            int row = tblTaiKhoan.getSelectedRow();
            String UserName = tblTaiKhoan.getValueAt(row, 0).toString();
            TaiKhoan tk = tkdao.getTaiKhoanbyUsername(UserName);

            techUserName.setText(tk.getUsername());
            if (techUserName.getText().equals("admin")) {
                techUserName.setEnabled(false);
                rd2.setEnabled(false);
                btnDelete.setEnabled(false);
            } else {
                techUserName.setEnabled(true);
                rd2.setEnabled(true);
                btnDelete.setEnabled(true);
            }

            techPassword.setText(tk.getPassword());
            if (tk.getLevel() == 1) {
                rd1.setSelected(true);
                rd2.setSelected(false);
            } else {
                rd2.setSelected(true);
                rd1.setSelected(false);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblTaiKhoanMouseClicked


