    private void tblNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblNhanVienMouseClicked
        // TODO add your handling code here:
        try {
            NhanVienDAO nvdao = new NhanVienDAO();
            int row = tblNhanVien.getSelectedRow();
            String MaNV = tblNhanVien.getValueAt(row, 0).toString();
            NhanVien nv = nvdao.getNhanVienbyMaNV(MaNV);
            
            techMaNV.setText(nv.getManv());
            if (techMaNV.getText().equals("admin")) {
                techMaNV.setEnabled(false);
                btnDelete.setEnabled(false);
            }else{
                techMaNV.setEnabled(true);
                btnDelete.setEnabled(true);
            }
            techTenNV.setText(nv.getTennv());
            
            if (nv.getGioitinh().equals("Nam")) {
                rdNam.setSelected(true);
                rdNu.setSelected(false);
            } else if (nv.getGioitinh().equals("Nữ")) {
                rdNu.setSelected(true);
                rdNam.setSelected(false);
            }
            String NgaySinh1 = tblNhanVien.getValueAt(row, 3).toString();
            Date date1 = new SimpleDateFormat("dd-MM-yyyy").parse(NgaySinh1);
            datechoose1.setDate(date1);
            techSdt.setText(nv.getSdt());
            techDiaChi.setText(nv.getDiachi());
            if (nv.getTrangthai().equals("Đang làm việc")) {
                rdDoing.setSelected(true);
                rdNghi.setSelected(false);
            } else if (nv.getTrangthai().equals("Đã nghỉ việc")) {
                rdNghi.setSelected(true);
                rdDoing.setSelected(false);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_tblNhanVienMouseClicked

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            String MaNV = techMaNV.getText();
            String TenNV = techTenNV.getText();
            String GioiTinh = null;
            if (rdNam.isSelected()) {
                GioiTinh = "Nam";
            } else if (rdNu.isSelected()) {
                GioiTinh = "Nữ";
            }

            String NgaySinh1 = df.format(datechoose1.getDate());
            //java.util.date
            java.util.Date NgaySinh2 = df.parse(NgaySinh1);
            //java,sql.date
            java.sql.Date NgaySinh = new java.sql.Date(NgaySinh2.getTime());

            String Sdt = techSdt.getText();
            String DiaChi = techDiaChi.getText();
            String TrangThai = null;
            if (rdDoing.isSelected()) {
                TrangThai = "Đang làm việc";
            } else if (rdNghi.isSelected()) {
                TrangThai = "Đã nghỉ việc";
            }

            NhanVien nv1 = new NhanVien(MaNV, TenNV, GioiTinh, Sdt, DiaChi, TrangThai, NgaySinh);
            NhanVienDAO.insert(nv1);
            shownv();
        } catch (ParseException ex) {
            Logger.getLogger(jp_QuanLyNhanVien.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

