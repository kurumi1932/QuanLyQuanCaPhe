    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String TuKhoa = "%" + txtSearch.getText() + "%";

        List<NhanVien> nhanvienList = NhanVienDAO.findbyTenNV(TuKhoa);
        tableModel.setRowCount(0);
        nhanvienList.forEach(nv -> {
            tableModel.addRow(new Object[]{nv.getManv(), nv.getTennv(), nv.getGioitinh(), 
                df.format(nv.getNgaysinh()), nv.getSdt(), nv.getDiachi(), nv.getTrangthai()});
        });
    }//GEN-LAST:event_txtSearchKeyReleased

